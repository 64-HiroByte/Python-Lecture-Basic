# 文字列(Strings)

print("hello world")
print('hello world')
print('1')

print("I'm fine")
print('I"m fine')

print("""
Hello world!!
How are you doing?
""")

print('hello \nworld')

print('hello \tworld')
# \を2つ重ねるとエスケープ文字 \ を出力できる
print('back slash n: \\n')

print('i\'m fine')

print('hello' + 'world' + '!!')
